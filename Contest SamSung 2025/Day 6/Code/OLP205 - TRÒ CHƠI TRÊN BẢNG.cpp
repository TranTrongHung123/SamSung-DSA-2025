#define FastIO(); ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
using namespace std;

const int inf = (int)1e18;
const int maxn = 1001;

int dx[4] = {-1, 0, 0, 1};
int dy[4] = {0, -1, 1, 0};

int n, m, q, res[maxn][maxn];
char a[maxn][maxn];

void BFS(vector<pair<int, int>> pos){
    deque<pair<int, int>> dq;
    for(auto [i, j] : pos){
        dq.push_back({i, j});
    }
    while(dq.size()){
        auto [i, j] = dq.front(); dq.pop_front();
        for(int k = 0; k < 4; k++){
            int i1 = i + dx[k];
            int j1 = j + dy[k];
            if(i1 < 1 || i1 > n || j1 < 1 || j1 > m) continue;
            int x = (a[i1][j1] == a[i][j] ? 0 : 1);
            if(res[i1][j1] > res[i][j] + x){
                res[i1][j1] = res[i][j] + x;
                if(x == 0){
                    dq.push_front({i1, j1});
                }
                else{   
                    dq.push_back({i1, j1});
                }
            }
        }
    }
}

TranHungss(){
    FastIO();
    cin >> n >> m >> q;
    for(int i = 1; i <= n; i++){
        for(int j = 1; j <= m; j++){
            cin >> a[i][j];
            if(a[i][j] == '*'){
                res[i][j] = 0;
            }
            else{
                res[i][j] = inf;
            }
        }
    }
    vector<pair<int, int>> pos;
    for(int i = 1; i <= n; i++){
        for(int j = 1; j <= m; j++){
            if(a[i][j] == '*'){
                pos.push_back({i, j});
            }
        }
    }
    BFS(pos);
    while(q--){
        int x, y;
        cin >> x >> y;
        cout << res[x][y] << endl;
    }
    return (0 ^ 0);
}

