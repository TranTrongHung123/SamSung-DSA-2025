int n, mod;

int mul(int a, int b){
    if(b == 0) return 0;
    int x = mul(a, b / 2) % mod;
    if(b & 1){
        return ((x + x) % mod + a % mod) % mod;
    }
    else{
        return (x + x) % mod;
    }   
}

struct Matrix {
    int x[2][2];

    Matrix() {}

    Matrix(int x[2][2]){
        for(int i = 0; i < 2; i++){
            for(int j = 0; j < 2; j++){
                this->x[i][j] = x[i][j];
            }
        }
    }

    friend Matrix operator * (Matrix a, Matrix b){
        Matrix res;
        for(int i = 0; i < 2; i++){
            for(int j = 0; j < 2; j++){
                res.x[i][j] = 0;
                for(int k = 0; k < 2; k++){
                    res.x[i][j] += mul(a.x[i][k], b.x[k][j]);;
                    res.x[i][j] %= mod;
                }
            }
        }
        return res;
    }
};

Matrix powMod(Matrix a, int n){
    int tmp[2][2] = {{1, 0}, {0, 1}};
    Matrix res(tmp);
    while(n){
        if(n & 1){
            res = res * a;
        }
        a = a * a;
        n /= 2;
    }
    return res;
}

