#define FastIO(); ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
using namespace std;

const int maxn = 100;

int a, b, k, C[maxn][maxn];

void init(){
    for(int i = 0; i <= 60; i++){
        for(int j = 0; j <= i; j++){
            if(j == 0 || j == i){
                C[i][j] = 1;
            }
            else{
                C[i][j] = C[i - 1][j] + C[i - 1][j - 1];
            }
        }
    }
}

TranHungss(){
    FastIO();
    init();
    cin >> a >> b >> k;
    string res = "";
    while(a > 0 || b > 0){
        if(a == 0){
            res += 'b';
            --b;
        }
        else if(b == 0){
            res += 'a';
            --a;
        }   
        else{
            int x = C[a + b - 1][a - 1];
            if(x >= k){
                res += 'a';
                --a;
            }
            else{
                res += 'b';
                k -= x;
                --b;
            }
        }
    }
    cout << res << endl;
    return (0 ^ 0);
}

