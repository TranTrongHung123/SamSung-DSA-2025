map<int, int> Phan_Tich(int n){
    map<int, int> mp;
    for(int i = 2; i <= sqrt(n); i++){
        if(n % i == 0){
            while(n % i == 0){
                ++mp[i];
                n /= i;
            }
        }
    }
    if(n != 1) ++ mp[n];
    return mp;
}

int Legendre(int n, int p){
    int res = 0;
    for(int i = p; i <= n; i *= p){
        res += n / i;
    }
    return res;
}

int solve(int n, int k){
    int res = LLONG_MAX;
    map<int, int> mp = Phan_Tich(k);
    for(auto [p, deg] : mp){
        res = min(res, Legendre(n, p) / deg);
    }
    return res;
}

