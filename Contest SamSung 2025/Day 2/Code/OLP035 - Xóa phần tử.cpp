int lcm(int a, int b){
    return a / __gcd(a, b) * b;
}

bool check(int x, int a[], int n, int k){
    int cnt = 0;
    for(int mask = 1; mask < (1 << n); mask++){
        int tmp = 1;
        for(int i = 0; i < n; i++){
            if(mask & (1 << i)){
                tmp = lcm(tmp, a[i]);
            }
        }
        if(__builtin_popcount(mask) & 1){
            cnt += x / tmp;
        }
        else{
            cnt -= x / tmp;
        }
    }
    return x - cnt >= k;
}

TranHungss(){
    FastIO();
    int TC; cin >> TC;
    while(TC--){
        int n, k;
        cin >> n >> k;
        int a[n];
        for(int &x : a) cin >> x;
        int res = 0, left = 1, right = 1e18;
        while(left <= right){
            int mid = (left + right) >> 1;
            if(check(mid, a, n, k)){
                res = mid;
                right = mid - 1;
            }
            else{
                left = mid + 1;
            }
        }
        cout << res << endl;
    }
    return (0 ^ 0);
}

