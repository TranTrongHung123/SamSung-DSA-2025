using namespace std;

const int maxn = 10000001;

int f[maxn];
vector<int> prime;

void sieve(){
    for(int i = 2; i < maxn; i++){
        f[i] = 1;
    }
    for(int i = 2; i <= sqrt(maxn); i++){
        if(f[i]){
            prime.push_back(i);
            for(int j = i * i; j <= maxn; j += i){
                f[j] = 0;
            }
        }
    }
    for(int i = sqrt(maxn) + 1; i <= maxn; i++){
        if(f[i]){
            prime.push_back(i);
        }
    }
}

map<int, int> Phan_Tich(int n){
    map<int, int> mp;
    for(int p : prime){
        if(p * p > n) break;
        if(n % p == 0){
            while(n % p == 0){
                ++mp[p];
                n /= p;
            }
        }
    }
    if(n != 1) ++mp[n];
    return mp;
}

TranHungss(){
    FastIO();
    sieve();
    int TC; cin >> TC;
    while(TC--){
        int n; cin >> n;
        map<int, int> res = Phan_Tich(n);
        for(auto [p, d] : res){
            cout << p << " " << d << endl;
        }
        cout << endl;
    }
    return (0 ^ 0);
}

