const int maxn = 10000001;

int f[maxn];
vector<int> prime;

void sieve(){
    for(int i = 2; i < maxn; i++){
        f[i] = 1;
    }
    for(int i = 2; i <= sqrt(maxn); i++){
        if(f[i]){
            prime.push_back(i);
            for(int j = i * i; j <= maxn; j += i){
                f[j] = 0;
            }
        }
    }
    for(int i = sqrt(maxn) + 1; i <= maxn; i++){
        if(f[i]){
            prime.push_back(i);
        }
    }
}

void Phan_Tich(int n, map<int, int> &mp){
    for(int p : prime){
        if(p * p > n) break;
        if(n % p == 0){
            while(n % p == 0){
                ++mp[p];
                n /= p;
            }
        }
    }
    if(n != 1) ++mp[n];
}

TranHungss(){
    FastIO();
    sieve();
    int n, m;
    cin >> n >> m;
    map<int, int> pos;
    for(int i = 0; i < n; i++){
        int x; cin >> x;
        Phan_Tich(x, pos);
    }
    vector<int> res;
    for(int t = 1; t <= m; t++){
        map<int, int> tmp;
        for(int i = 1; i <= n; i++){
            int x; cin >> x;
            Phan_Tich(x, tmp);
        }
        bool check = true;
        for(auto [p, deg] : tmp){
            if(pos[p] < deg){
                check = false;
                break;
            }
        }
        if(check) res.push_back(t);
    }
    cout << res.size() << endl;
    for(auto x : res) cout << x << " ";
    return (0 ^ 0);
}

