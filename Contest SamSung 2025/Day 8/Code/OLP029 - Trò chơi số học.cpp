const int inf = (int)1e18;
const int maxn = 25;

int n, k, mod, arr[maxn];

struct Matrix {
    int x[maxn][maxn];

    Matrix(){
        memset(x, 0, sizeof(x));
    }

    friend Matrix operator* (Matrix a, Matrix b){
        Matrix res;
        for(int i = 1; i <= n; i++){
            for(int j = 1; j <= n; j++){
                __int128 sum = 0;
                for(int k = 1; k <= n; k++){
                    sum += (__int128)a.x[i][k] * (__int128)b.x[k][j];
                    sum %= mod;
                }
                res.x[i][j] = sum;
            }
        }
        return res;
    }

    friend Matrix operator+ (Matrix a, Matrix b){
        Matrix res;
        for(int i = 1; i <= n; i++){
            for(int j = 1; j <= n; j++){
                res.x[i][j] = a.x[i][j] + b.x[i][j];
            }
        }
        return res;
    }
};

bool isPrime(int x){
    if(x <= 1) return false;
    for(int i = 2; i <= sqrt(x); i++){
        if(x % i == 0){
            return false;
        }
    }
    return true;
}

// {a^k, s(k) = a^1 + a^2 + ... + a^k}
// s(k) = s(k - 1) + a^k (if k odd)
// s(k) = s(k / 2) + s(k / 2) * a^(k / 2) (if k even) 
pair<Matrix, Matrix> sumPow(Matrix a, int k){
    if(k == 1){
        return {a, a};
    }
    if(k & 1){
        auto [Ak, Sk] = sumPow(a, k - 1);
        Ak = Ak * a;
        return {Ak, Sk + Ak};
    }
    else{
        auto [Ak, Sk] = sumPow(a, k >> 1);
        return {Ak * Ak, Sk + Ak * Sk};
    }
}

