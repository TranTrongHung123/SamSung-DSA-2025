#include <bits/stdc++.h>
#define endl "\n"
#define int long long
#define double long double
#define TranHungss() signed main()
#define FastIO(); ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
using namespace std;

const int maxn = 100005;

int n, a[maxn], IT[4 * maxn];

void build(int i, int l, int r){
    if(l == r){
        IT[i] = a[l];
        return;
    }
    int m = (l + r) >> 1;
    build(2 * i, l, m);
    build(2 * i + 1, m + 1, r);
    IT[i] = IT[2 * i] + IT[2 * i + 1];
}

void update(int i, int l, int r, int pos, int val){
    if(l == r){
        IT[i] += val;
        return;
    }
    int m = (l + r) >> 1;
    if(pos <= m){
        update(2 * i, l, m, pos, val);
    }
    else{
        update(2 * i + 1, m + 1, r, pos, val);
    }
    IT[i] = IT[2 * i] + IT[2 * i + 1];
}

int getSum(int i, int l, int r, int u, int v){
    if(l > v || r < u) return 0;
    if(l >= u && r <= v){
        return IT[i];
    }
    int m = (l + r) >> 1;
    int s1 = getSum(2 * i, l, m, u, v);
    int s2 = getSum(2 * i + 1, m + 1, r, u, v);
    return s1 + s2;
}

TranHungss(){
    FastIO();
    cin >> n;
    for(int i = 1; i <= n; i++) cin >> a[i];
    build(1, 1, n);
    int q; cin >> q;
    while(q--){
        int type, u, v;
        cin >> type >> u >> v;
        if(type == 1){
            update(1, 1, n, u, v);
        }
        else{
            cout << getSum(1, 1, n, u, v) << endl;
        }
    }
    return (0 ^ 0);
}

