#include <bits/stdc++.h>
#define endl "\n"
#define int long long
#define double long double
#define TranHungss() signed main()
#define FastIO(); ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
using namespace std;

const int maxn = 200002;

int n, ax, ay, bx, by, q, tmp[maxn];
pair<int, int> a[maxn], dis[maxn];
vector<int> IT[4 * maxn];

bool cmp(pair<int, int> x, pair<int, int> y){
    if(x.first != y.first) return x.first < y.first;
    return x.second < y.second;
}

void build(int i, int l, int r){
    if(l == r){
        IT[i].push_back(dis[l].second);
        return;
    }
    int m = (l + r) >> 1;
    build(2 * i, l, m);
    build(2 * i + 1, m + 1, r);
    merge(begin(IT[2 * i]), end(IT[2 * i]), begin(IT[2 * i + 1]), end(IT[2 * i + 1]), back_inserter(IT[i]));
}

int get(int i, int l, int r, int u, int v, int x){
    if(l > v || r < u) return 0;
    if(l >= u && r <= v){
        auto it = upper_bound(begin(IT[i]), end(IT[i]), x);
        return distance(it, IT[i].end());
    }
    int m = (l + r) >> 1;
    int s1 = get(2 * i, l, m, u, v, x);
    int s2 = get(2 * i + 1, m + 1, r, u, v, x);
    return s1 + s2;
}

TranHungss(){
    FastIO();
    cin >> n;
    for(int i = 1; i <= n; i++) cin >> a[i].first >> a[i].second;
    cin >> ax >> ay >> bx >> by >> q;
    for(int i = 1; i <= n; i++){
        int d1 = (a[i].first - ax) * (a[i].first - ax) + (a[i].second - ay) * (a[i].second - ay);
        int d2 = (a[i].first - bx) * (a[i].first - bx) + (a[i].second - by) * (a[i].second - by);
        dis[i] = {d1, d2};
    }
    sort(dis + 1, dis + n + 1, cmp);
    for(int i = 1; i <= n; i++){
        tmp[i] = dis[i].first;
    }
    build(1, 1, n);
    while(q--){
        int r1, r2;
        cin >> r1 >> r2;
        auto pos = upper_bound(tmp + 1, tmp + n + 1, r1 * r1) - tmp;
        if(pos > n){
            cout << n << endl;
            continue;
        }
        cout << n - get(1, 1, n, pos, n, r2 * r2) << endl;
    }
    return (0 ^ 0);
}

