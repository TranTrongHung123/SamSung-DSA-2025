#include <bits/stdc++.h>
#define endl "\n"
#define int long long
#define double long double
#define TranHungss() signed main()
#define FastIO(); ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
using namespace std;

const int maxn = 100005;

int n, a[maxn];
vector<int> IT[4 * maxn];

void build(int i, int l, int r){
    if(l == r){
        IT[i].push_back(a[l]);
        return;
    }
    int m = (l + r) >> 1;
    build(2 * i, l, m);
    build(2 * i + 1, m + 1, r);
    merge(begin(IT[2 * i]), end(IT[2 * i]), begin(IT[2 * i + 1]), end(IT[2 * i + 1]), back_inserter(IT[i]));
}

int get1(int i, int l, int r, int u, int v, int x){
    if(l > v || r < u) return 0;
    if(l >= u && r <= v){
        auto it = upper_bound(begin(IT[i]), end(IT[i]), x);
        return distance(it, IT[i].end());
    }
    int m = (l + r) >> 1;
    int s1 = get1(2 * i, l, m, u, v, x);
    int s2 = get1(2 * i + 1, m + 1, r, u, v, x);
    return s1 + s2;
}

int get2(int i, int l, int r, int u, int v, int x){
    if(l > v || r < u) return 0;
    if(l >= u && r <= v){
        auto it = lower_bound(begin(IT[i]), end(IT[i]), x);
        return distance(IT[i].begin(), it);
    }
    int m = (l + r) >> 1;
    int s1 = get2(2 * i, l, m, u, v, x);
    int s2 = get2(2 * i + 1, m + 1, r, u, v, x);
    return s1 + s2;
}

TranHungss(){
    FastIO();
    int TC; cin >> TC;
    while(TC--){
        for(int i = 0; i < 4 * maxn; i++){
            IT[i].clear();
        }
        cin >> n;
        for(int i = 1; i <= n; i++) cin >> a[i];
        build(1, 1, n);
        int res = 0;
        for(int i = 2; i <= n - 1; i++){
            auto x1 = get1(1, 1, n, 1, i - 1, a[i]);
            auto x2 = get2(1, 1, n, i + 1, n, a[i]);
            res += x1 * x2;
        }
        cout << res << endl;
    }
    return (0 ^ 0);
}

