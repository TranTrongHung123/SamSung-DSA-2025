using namespace std;

const int maxn = 105;

int n, m;
int BIT[maxn][maxn][maxn], A[maxn][maxn][maxn];

void update(int x, int y, int z, int w){
    for(int i = x; i <= n; i += i & -i){
        for(int j = y; j <= n; j += j & -j){
            for(int k = z; k <= n; k += k & -k){
                BIT[i][j][k] += w;
            }
        }
    }
}

int sumQuery(int x, int y, int z){
    int sum = 0;
    for(int i = x; i > 0; i -= i & -i){
        for(int j = y; j > 0; j -= j & -j){
            for(int k = z; k > 0; k -= k & -k){
                sum += BIT[i][j][k];
            }
        }
    }
    return sum;
}

int matrixSum(int x1, int y1, int z1, int x2, int y2, int z2){
    if(x1 > x2) swap(x1, x2);
    if(y1 > y2) swap(y1, y2);
    if(z1 > z2) swap(z1, z2);
    return sumQuery(x2, y2, z2)
         - sumQuery(x1 - 1, y2, z2)
         - sumQuery(x2, y1 - 1, z2)
         - sumQuery(x2, y2, z1 - 1)
         + sumQuery(x1 - 1, y1 - 1, z2)
         + sumQuery(x1 - 1, y2, z1 - 1)
         + sumQuery(x2, y1 - 1, z1 - 1)
         - sumQuery(x1 - 1, y1 - 1, z1 - 1);
}

signed main(){
    FastIO();
    int TC; cin >> TC;
    while(TC--){
        memset(BIT, 0, sizeof(BIT));
        memset(A, 0, sizeof(A));
        cin >> n >> m;
        while(m--){
            string s; cin >> s;
            if(s == "UPDATE"){
                int x, y, z, w;
                cin >> x >> y >> z >> w;
                int delta = w - A[x][y][z];
                A[x][y][z] = w;
                update(x, y, z, delta);
            }
            else{
                int x1, y1, z1, x2, y2, z2;
                cin >> x1 >> y1 >> z1 >> x2 >> y2 >> z2;
                cout << matrixSum(x1, y1, z1, x2, y2, z2) << endl;
            }
        }
    }
    return (0 ^ 0);
}

