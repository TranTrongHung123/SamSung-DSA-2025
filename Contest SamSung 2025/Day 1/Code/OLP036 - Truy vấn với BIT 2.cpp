#include <bits/stdc++.h>
#define endl "\n"
#define int long long
#define double long double
#define TranHungss() signed main()
#define FastIO(); ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
using namespace std;

const int maxn = 100005;

int n, a[maxn], IT[4 * maxn], lazy[4 * maxn];

void build(int i, int l, int r){
    if(l == r){
        IT[i] = a[l];
        return;
    }
    int m = (l + r) >> 1;
    build(2 * i, l, m);
    build(2 * i + 1, m + 1, r);
    IT[i] = IT[2 * i] + IT[2 * i + 1];
}

void push(int i){
    if(lazy[i] == 0) return;
    IT[2 * i] += lazy[i];
    IT[2 * i + 1] += lazy[i];
    lazy[2 * i] += lazy[i];
    lazy[2 * i + 1] += lazy[i];
    lazy[i] = 0;
}

void update(int i, int l, int r, int u, int v, int val){
    if(l > v || r < u) return;
    if(l >= u && r <= v){
        IT[i] += val;
        lazy[i] += val;
        return;
    }
    push(i);
    int m = (l + r) >> 1;
    update(2 * i, l, m, u, v, val);
    update(2 * i + 1, m + 1, r, u, v, val);
    IT[i] = IT[2 * i] + IT[2 * i + 1];
}

int getVal(int i, int l, int r, int pos){
    if(l == r){
        return IT[i];
    }
    push(i);
    int m = (l + r) >> 1;
    if(pos <= m){
        return getVal(2 * i, l, m, pos);
    }
    else{
        return getVal(2 * i + 1, m + 1, r, pos);
    }
}

TranHungss(){
    FastIO();
    cin >> n;
    for(int i = 1; i <= n; i++) cin >> a[i];
    build(1, 1, n);
    int q; cin >> q;
    while(q--){
        int type; cin >> type;
        if(type == 1){
            int u, v, val;
            cin >> u >> v >> val;
            update(1, 1, n, u, v, val);
        }
        else{
            int pos; cin >> pos;
            cout << getVal(1, 1, n, pos) << endl;
        }
    }
    return (0 ^ 0);
}

