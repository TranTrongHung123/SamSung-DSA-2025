using namespace std;

const int maxn = 100001;

int n, m, a[maxn];
pair<int, int> IT[4 * maxn];

pair<int, int> merge(const pair<int, int> &a, const pair<int, int> &b){
    vector<int> vt = {a.first, a.second, b.first, b.second};
    sort(begin(vt), end(vt), greater<int>());
    return {vt[0], vt[1]};
}

void build(int i, int l, int r){
    if(l == r){
        IT[i] = {a[l], LLONG_MIN};
    }
    else{
        int m = (l + r) >> 1;
        build(2 * i, l, m);
        build(2 * i + 1, m + 1, r);
        IT[i] = merge(IT[2 * i], IT[2 * i + 1]);
    }
}

void update(int i, int l, int r, int pos, int val){
    if(l == r){
        IT[i] = {val, LLONG_MIN};
    }
    else{
        int m = (l + r) >> 1;
        if(pos <= m){
            update(2 * i, l, m, pos, val);
        }
        else{
            update(2 * i + 1, m + 1, r, pos, val);
        }
        IT[i] = merge(IT[2 * i], IT[2 * i + 1]);
    }
}

pair<int, int> getAns(int i, int l, int r, int u, int v){
    if(l > v || r < u) return {LLONG_MIN, LLONG_MIN};
    if(l >= u && r <= v) return IT[i];
    int m = (l + r) >> 1;
    auto s1 = getAns(2 * i, l, m, u, v);
    auto s2 = getAns(2 * i + 1, m + 1, r, u, v);
    return merge(s1, s2);
}

TranHungss(){
    FastIO();
    cin >> n >> m;
    for(int i = 1; i <= n; i++) cin >> a[i];
    build(1, 1, n);
    while(m--){
        char c; cin >> c;
        if(c == 'U'){
            int pos, val;
            cin >> pos >> val;
            update(1, 1, n, pos, val);
        }
        else{
            int u, v;
            cin >> u >> v;
            auto [x, y] = getAns(1, 1, n, u, v);
            cout << x + y << endl;
        }
    }
    return (0 ^ 0);
}

