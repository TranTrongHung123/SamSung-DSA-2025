using namespace std;

const int maxn = 1000001;

struct Node{
    int maxLen, open, close;
} IT[4 * maxn];

int n, q;
string s;

Node merge(const Node &a, const Node &b){
    Node res;
    int tmp = min(a.open, b.close);
    res.maxLen = a.maxLen + b.maxLen + 2 * tmp;
    res.open = b.open + a.open - tmp;
    res.close = a.close + b.close - tmp;
    return res;
}

void build(int i, int l, int r){
    if(l == r){
        if(s[l] == '('){
            IT[i] = {0, 1, 0};
        }
        else{   
            IT[i] = {0, 0, 1};
        }
    }
    else{
        int m = (l + r) >> 1;
        build(2 * i, l, m);
        build(2 * i + 1, m + 1, r);
        IT[i] = merge(IT[2 * i], IT[2 * i + 1]);
    }
}

Node getAns(int i, int l, int r, int u, int v){
    if(l > v || r < u) return {0, 0, 0};
    if(l >= u && r <= v) return IT[i];
    int m = (l + r) >> 1;
    auto s1 = getAns(2 * i, l, m, u, v);
    auto s2 = getAns(2 * i + 1, m + 1, r, u, v);
    return merge(s1, s2);
}

TranHungss(){
    FastIO();
    cin >> s;
    n = s.size();
    s = " "  + s;
    build(1, 1, n);
    cin >> q;
    while(q--){
        int u, v;
        cin >> u >> v;
        auto res = getAns(1, 1, n, u, v);
        cout << res.maxLen << endl;
    }
    return (0 ^ 0);
}

