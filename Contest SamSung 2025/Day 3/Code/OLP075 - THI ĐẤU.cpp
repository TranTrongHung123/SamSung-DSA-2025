#define __builtin_popcount __builtin_popcountll
#define join(x, y, z); merge(begin(x), end(x), begin(y), end(y), back_inserter(z));
#define FastIO(); ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
using namespace std;

const int maxn = 1005;
const int mod = 1e9 + 9;

int n, m, k, a[maxn], b[maxn], dp[maxn][maxn][20];

TranHungss(){
    FastIO();
    cin >> n >> m >> k;
    for(int i = 1; i <= n; i++) cin >> a[i];
    for(int i = 1; i <= m; i++) cin >> b[i];
    sort(a + 1, a + n + 1);
    sort(b + 1, b + m + 1);
    for(int i = 0; i <= n; i++){
        for(int j = 0; j <= m; j++){
            dp[i][j][0] = 1;
        }
    }
    for(int i = 1; i <= n; i++){
        for(int j = 1; j <= m; j++){
            for(int t = 0; t <= k; t++){
                dp[i][j][t] = (dp[i-1][j][t] + dp[i][j-1][t] - dp[i-1][j-1][t] + mod) % mod;
                if(t != 0 && a[i] > b[j]){
                    dp[i][j][t] += dp[i-1][j-1][t-1];
                    dp[i][j][t] %= mod;
                }
            }
        }
    }
    cout << dp[n][m][k] << endl;
    return (0 ^ 0);
}

