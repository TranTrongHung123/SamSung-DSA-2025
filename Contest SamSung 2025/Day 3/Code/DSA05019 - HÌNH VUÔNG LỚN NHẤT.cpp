#define FastIO(); ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
using namespace std;

TranHungss(){
    FastIO();
    int TC; cin >> TC;
    while(TC--){
        int n, m;
        cin >> n >> m;
        int a[n][m];
        for(int i = 0; i < n; i++){
            for(int j = 0; j < m; j++){
                cin >> a[i][j];
            }
        }
        int dp[n][m];
        for(int j = 0; j < m; j++){
            dp[0][j] = a[0][j];
        }
        for(int i = 0; i < n; i++){
            dp[i][0] = a[i][0];
        }
        for(int i = 0; i < n; i++){
            for(int j = 0; j < m; j++){
                if(a[i][j]){
                    dp[i][j] = 1 + min({dp[i - 1][j - 1], dp[i - 1][j], dp[i][j - 1]});
                }
                else{
                    dp[i][j] = 0;
                }
            }
        }
        int res = 0;
        for(int i = 0; i < n; i++){
            for(int j = 0; j < m; j++){
                res = max(res, dp[i][j]);
            }
        }
        cout << res << endl;
    }
    return (0 ^ 0);
}

